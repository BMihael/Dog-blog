  <h2>Origin</h2>
  <p>
  Main article: Origin of the domestic dog
  The domestic dogs origin includes the dogs genetic divergence from the wolf, its domestication, and its development into dog types and dog breeds. The dog is a member of the genus Canis, which forms part of the wolf-like canids, and was the first species and the only large carnivore to have been domesticated. Genetic studies comparing dogs with modern wolves show reciprocal monophyly (separate groups), which implies that dogs are not genetically close to any living wolf and that their wild ancestor is extinct. An extinct Late Pleistocene wolf may have been the dogs ancestor with the dogs similarity to the extant grey wolf being the result of genetic admixture between the two. In 2020, a literature review of canid domestication stated that modern dogs were not descended from the same Canis lineage as modern wolves, and proposes that dogs may be descended from a Pleistocene wolf closer in size to a village dog
  </p>
  <p>
  The genetic divergence between dogs and wolves occurred between 20,000 and 40,000 years ago, just before or during the Last Glacial Maximum (20,000–27,000 years ago). This period represents the upper time-limit for domestication commencement because it is the time of divergence and not the time of domestication, which occurred later.One of the most critical transitions in human history was the domestication of animals, which began with the long-term association between wolves and hunter–gatherers more than 15,000 years ago.[28] The archaeological record and genetic analysis show the remains of the Bonn–Oberkassel dog buried beside humans 14,200 years ago to be the first undisputed dog, with disputed remains occurring 36,000 years ago. By 11,000 years ago, there were five distinct dog lineages all sharing a common ancestry distinct from present-day wolves.
